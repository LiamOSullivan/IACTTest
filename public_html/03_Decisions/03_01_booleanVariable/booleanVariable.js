var value1 = true; //declare a variable and assign it 'true'
var value2 = false; //declare a variable and assign it 'false'

println("Value 1 is: "+value1);
println("Value 2 is: "+value2); 

//Invert the vlaue of a boolean using the NOT operator '!'
println("Value 1 inverted (NOT) is: "+!value1);

value1 = !value1; //invert the stored value using the NOT operator
println("Stored Value 1 inverted (NOT) is: "+value1);
